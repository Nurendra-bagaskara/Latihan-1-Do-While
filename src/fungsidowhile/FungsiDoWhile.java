package fungsidowhile;

public class FungsiDoWhile {

    public static void main(String[] args) {
        // Deklarasi dan inisialisasi Variabel
        int bil = 1;
        //Perulangan
        do {
        // statement
            System.out.println(bil+" ");
            bil++;
        }while ( bil <=10 );
    }
    
}
